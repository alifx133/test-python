# ตัวแปรเก็บผลรวม
total = 0

# ใช้ while loop รับตัวเลขจากผู้ใช้
while True:
    num = int(input("ป้อนตัวเลข (0 เพื่อหยุด): "))
    if num == 0:
        break  # หยุดการทำงานเมื่อพิมพ์ 0
    total += num  # เพิ่มค่าตัวเลขที่ป้อนลงในผลรวม

# แสดงผลรวม
print("ผลรวมของตัวเลขทั้งหมด:", total)
